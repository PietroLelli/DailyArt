package com.example.dailyart.ArtWorkRecyclerView;

public class ArtWork {
    String image;
    String name;
    String museum;
    String artist;
    String description;
    String guide;
    String imageMuseum;

    public ArtWork() {

    }

    public String getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

    public String getMuseum() {
        return museum;
    }

    public String getArtist() {
        return artist;
    }

    public String getDescription() {
        return description;
    }

    public String getGuide() {
        return guide;
    }

    public String getImageMuseum() { return imageMuseum; }
}
